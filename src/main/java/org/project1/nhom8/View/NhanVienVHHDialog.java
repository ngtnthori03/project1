    public void fillTable(List<NhanVien>list){
        model = (DefaultTableModel) tblNhanVienVHH.getModel();
        model.setRowCount(0);
        for (NhanVien nhanVien : list) {
            model.addRow(nhanVien.toDataRow());
        }
    }

